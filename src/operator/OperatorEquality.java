package operator;

public class OperatorEquality {
    public static void main(String[] args) {
        int a = 10;
        int b = 11;
        System.out.println(a==10);
        System.out.println(a==11);
        System.out.println(a==b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(10>11);
        System.out.println(10<11);
        System.out.println(a!=10);
        System.out.println(a!=11);
    }
}
