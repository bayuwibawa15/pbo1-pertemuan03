package operator;

public class OperatorUnary {
    public static void main(String[] args) {
        int i = 15;
        i++;
        System.out.println(i);
        i++;
        System.out.println(i);
        i=i+3;
        System.out.println(i);
        i+=4;
        System.out.println(i);
        i*=1;
        System.out.println(i);
    }
}
